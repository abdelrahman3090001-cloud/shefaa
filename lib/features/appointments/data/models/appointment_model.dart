class AppointmentModel {
  final String doctorName;
  final String specialization;
  final String location;
  final String time;
  final String day;
  final String month;
  final String date;
  final String status;

  AppointmentModel({
    required this.doctorName,
    required this.specialization,
    required this.location,
    required this.time,
    required this.day,
    required this.month,
    required this.date,
    required this.status,
  });
}
